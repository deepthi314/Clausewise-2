def simplify_text(text: str, mode: str, lang: str = "en", category: str = None) -> str:
    if lang != "en" and category in TRANSLATIONS and lang in TRANSLATIONS[category]:
        if mode in TRANSLATIONS[category][lang]:
            return TRANSLATIONS[category][lang][mode]

    if mode == "ELI5":
        return f"Imagine this like a playground rule. {text}"
    if mode == "Professional":
        return f"In legal terms, this implies... {text}"
    return text

